package com.shopping.view;

public class CartViewPojo {
	private int id;
	private String name;
	private Double price;
	private int num;
	private Double money;

	public CartViewPojo() {
		super();
	}

	public CartViewPojo(int id, String name, Double price, int num, Double money) {
		super();
		this.id = id;
		this.name = name;
		this.price = price;
		this.num = num;
		this.money = money;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public Double getMoney() {
		return money;
	}

	public void setMoney(Double money) {
		this.money = money;
	}

	@Override
	public String toString() {
		return "CartView [id=" + id + ", name=" + name + ", price=" + price
				+ ", num=" + num + ", money=" + money + "]";
	}

}
