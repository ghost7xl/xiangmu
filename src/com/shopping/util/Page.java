package com.shopping.util;

public class Page {
	private int currentPage;// 当前页码
	private int pageCount;// 每页显示数量
	private boolean hasNextPage;// 是否有下一页
	private boolean hasLastPage;// 是否有上一页
	private int totalCount;// 总数量
	private int totalPage;// 总页数
	private int startIndex;// 开始索引
	public Page() {
		super();
	}
	public Page(int currentPage, int pageCount, boolean hasNextPage,
			boolean hasLastPage, int totalCount, int totalPage, int startIndex) {
		super();
		this.currentPage = currentPage;
		this.pageCount = pageCount;
		this.hasNextPage = hasNextPage;
		this.hasLastPage = hasLastPage;
		this.totalCount = totalCount;
		this.totalPage = totalPage;
		this.startIndex = startIndex;
	}
	public int getCurrentPage() {
		return currentPage;
	}
	public void setCurrentPage(int currentPage) {
		this.currentPage = currentPage;
	}
	public int getPageCount() {
		return pageCount;
	}
	public void setPageCount(int pageCount) {
		this.pageCount = pageCount;
	}
	public boolean isHasNextPage() {
		return hasNextPage;
	}
	public void setHasNextPage(boolean hasNextPage) {
		this.hasNextPage = hasNextPage;
	}
	public boolean isHasLastPage() {
		return hasLastPage;
	}
	public void setHasLastPage(boolean hasLastPage) {
		this.hasLastPage = hasLastPage;
	}
	public int getTotalCount() {
		return totalCount;
	}
	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}
	public int getTotalPage() {
		return totalPage;
	}
	public void setTotalPage(int totalPage) {
		this.totalPage = totalPage;
	}
	public int getStartIndex() {
		return startIndex;
	}
	public void setStartIndex(int startIndex) {
		this.startIndex = startIndex;
	}

	

}
