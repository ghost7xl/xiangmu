package com.shopping.pojo;

import java.sql.Date;

public class Order {
	private int id;
	private int uid;
	private int uaid;
	private Date createtime;
	private double money;
	private int flag;

	public Order() {
		super();
	}

	public Order(int uid, int uaid, double money) {
		super();
		this.uid = uid;
		this.uaid = uaid;
		this.money = money;
	}

	public Order(int id, int uid, int uaid, Date createtime, double money,
			int flag) {
		super();
		this.id = id;
		this.uid = uid;
		this.uaid = uaid;
		this.createtime = createtime;
		this.money = money;
		this.flag = flag;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getUid() {
		return uid;
	}

	public void setUid(int uid) {
		this.uid = uid;
	}

	public int getUaid() {
		return uaid;
	}

	public void setUaid(int uaid) {
		this.uaid = uaid;
	}

	public Date getCreatetime() {
		return createtime;
	}

	public void setCreatetime(Date createtime) {
		this.createtime = createtime;
	}

	public double getMoney() {
		return money;
	}

	public void setMoney(double money) {
		this.money = money;
	}

	public int getFlag() {
		return flag;
	}

	public void setFlag(int flag) {
		this.flag = flag;
	}

	@Override
	public String toString() {
		return "Order [id=" + id + ", uid=" + uid + ", uaid=" + uaid
				+ ", createtime=" + createtime + ", money=" + money + ", flag="
				+ flag + "]";
	}

}
